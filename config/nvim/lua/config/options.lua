-- Clipboard: macOS (pbcopy/pbpaste)
if vim.fn.has('mac') == 1 then
  vim.g.clipboard = {
    name = 'MacClipboard',
    copy = {
      ['+'] = 'pbcopy',
      ['*'] = 'pbcopy',
    },
    paste = {
      ['+'] = 'pbpaste',
      ['*'] = 'pbpaste',
    },
    cache_enabled = 0,
  }
end
